package com.qlangtech.tis.solrextend.queryparse.s4shop;

import com.qlangtech.tis.solrextend.queryparse.s4shop.JedisFactory.EntityPreSellRule;
import com.google.common.base.Joiner;
import junit.framework.Assert;
import junit.framework.TestCase;

/* *
 * @author 百岁（baisui@qlangtech.com）
 * @date 2019年1月17日
 */
public class TestJedisFactory extends TestCase {

    public void testRedisGet() throws Exception {
        EntityPreSellRule rule = JedisFactory.getEntityPreSellRule("99001331");
        Assert.assertFalse(rule.isPseudo());
        if (rule.hasDate(1233)) {
            rule.getDate(1233).hasDiscount(22);
        }
        Assert.assertNotNull(rule);
        Joiner j = Joiner.on(",").skipNulls();
        System.out.println(j.join(rule.dateSet.keySet()));
        System.out.println(j.join(rule.discountSet));
        System.out.println(j.join(rule.hoursSet));
        System.out.println(j.join(rule.tableSet));
        System.out.println("=========================================");
        for (EntityPreSellRule v : rule.dateSet.values()) {
            System.out.println(j.join(v.dateSet.keySet()));
            System.out.println(j.join(v.discountSet));
            System.out.println(j.join(v.hoursSet));
            System.out.println(j.join(v.tableSet));
        }
    }
}
